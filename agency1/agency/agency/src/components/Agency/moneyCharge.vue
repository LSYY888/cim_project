	export default {
		mounted () {
			var flag = true
			$('.right-box i').on('click', function () {
				if (flag === true) {
					$(this).parents().siblings('.percer-section').addClass('open').removeClass('close')
					$(this).addClass('toptransform').removeClass('bottransform')
					flag = false
				} else {
					$(this).parents().siblings('.percer-section').addClass('close').removeClass('open')
					$(this).addClass('bottransform').removeClass('toptransform')
					flag = true
				}
			})
			$('.height-price input').on('blur', function () {
				$(this).addClass('color')
			})
			$('.percer-input').on('blur', function () {
				$(this).parents().siblings('.height-price-box').find('input').addClass('color')
				var percerValue = $(this).val()
				var inputDom = $(this).parents().siblings('.height-price-box').find('input')
				var inputDomVal = inputDom.val()
				inputDom.val((inputDomVal * percerValue) / 100)
			})
		}
	}
